/**
 * Fetch Hebrew word list from eyaler/hebrew_wordlists and generate dictionary.
 * Run: node scripts/build-dictionary.js
 *
 * Sources:
 *   nouns.txt, verbs_no_fatverb.txt, adjectives.txt
 *   from https://github.com/eyaler/hebrew_wordlists (AGPL-3.0)
 */
const fs = require('fs')
const path = require('path')
const https = require('https')

const BASE = 'https://raw.githubusercontent.com/eyaler/hebrew_wordlists/main/'
// Priority: master list first (base forms), then POS files (inflections).
const PRIORITY_FILES = ['all_no_fatverb.txt']
const EXTRA_FILES = [
  'nouns.txt',
  'verbs_no_fatverb.txt',
  'adjectives.txt',
  'prepositions.txt',
  'gerunds.txt',
]

function fetch(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      let data = ''
      res.on('data', (chunk) => (data += chunk))
      res.on('end', () => resolve(data))
      res.on('error', reject)
    }).on('error', reject)
  })
}

// Hebrew-only regex (no niqqud, no punctuation)
const HEB_ONLY = /^[\u05D0-\u05EA]+$/
const MIN_LEN = 2
const MAX_LEN = 8

async function fetchFile(file) {
  const url = BASE + file
  console.log(`  Fetching ${file}...`)
  const data = await fetch(url)
  return data.split('\n').map((l) => l.trim()).filter(Boolean)
}

function isValid(word) {
  return word.length >= MIN_LEN && word.length <= MAX_LEN && HEB_ONLY.test(word)
}

async function main() {
  console.log('Fetching Hebrew word lists...')

  // Step 1: Get ALL base forms from master list (these are the important ones)
  const priorityWords = new Set()
  for (const file of PRIORITY_FILES) {
    const lines = await fetchFile(file)
    for (const w of lines) { if (isValid(w)) priorityWords.add(w) }
    console.log(`    → ${lines.length} raw, ${priorityWords.size} valid 2-8 char`)
  }

  // Step 2: Get extra inflected forms from POS files
  const extraWords = new Set()
  for (const file of EXTRA_FILES) {
    const lines = await fetchFile(file)
    for (const w of lines) {
      if (isValid(w) && !priorityWords.has(w)) extraWords.add(w)
    }
    console.log(`    → ${lines.length} raw, ${extraWords.size} extra`)
  }

  // Combine all words
  const allWords = new Set([...priorityWords, ...extraWords])
  const all = Array.from(allWords).sort()
  console.log(`\nTotal unique: ${all.length}`)

  // Cap per-bucket. Keep ALL short words (most useful for game).
  // Longer words get sampled — missing an obscure 7-letter inflection is fine.
  function sample(arr, n) {
    if (n >= arr.length) return arr
    const step = arr.length / n
    return Array.from({ length: n }, (_, i) => arr[Math.floor(i * step)])
  }

  // Budget: ~250KB gzipped. All 2-4 char words (~25K), sample 5+ to ~20K more.
  const caps = { 2: Infinity, 3: Infinity, 4: Infinity, 5: 8000, 6: 5000, 7: 3000, 8: 2000 }
  const sorted = []
  for (let len = 2; len <= 8; len++) {
    const bucket = all.filter((w) => w.length === len)
    const cap = caps[len] || bucket.length
    sorted.push(...sample(bucket, cap))
  }
  sorted.sort()

  console.log(`Final dictionary: ${sorted.length} words`)
  console.log(`  2-char: ${sorted.filter((w) => w.length === 2).length}`)
  console.log(`  3-char: ${sorted.filter((w) => w.length === 3).length}`)
  console.log(`  4-char: ${sorted.filter((w) => w.length === 4).length}`)
  console.log(`  5-char: ${sorted.filter((w) => w.length === 5).length}`)
  console.log(`  6-char: ${sorted.filter((w) => w.length === 6).length}`)
  console.log(`  7-char: ${sorted.filter((w) => w.length === 7).length}`)
  console.log(`  8-char: ${sorted.filter((w) => w.length === 8).length}`)

  // Generate the TS file
  const outPath = path.join(__dirname, '..', 'lib', 'dictionary.ts')

  // Chunk the array into lines of ~15 words for readability
  const chunks = []
  for (let i = 0; i < sorted.length; i += 15) {
    const chunk = sorted.slice(i, i + 15).map((w) => `'${w}'`).join(',')
    chunks.push(chunk)
  }

  const ts = `/**
 * Hebrew word dictionary for Exacto.
 * ${sorted.length.toLocaleString()} words, lengths 2–8, no niqqud.
 * Source: github.com/eyaler/hebrew_wordlists (AGPL-3.0)
 * Generated by: scripts/build-dictionary.js
 *
 * API: isValidWord(), getAllWords(), getWordsFromLetters()
 * Swap the data source freely — only these functions are public.
 */

const WORDS: string[] = [
${chunks.map((c) => '  ' + c + ',').join('\n')}
]

const WORD_SET = new Set(WORDS)

/** Check if a word is valid (exists in dictionary). */
export function isValidWord(word: string): boolean {
  return WORD_SET.has(word)
}

/** Get all dictionary words (for solvability checks). */
export function getAllWords(): string[] {
  return WORDS
}

/**
 * Get all valid words that can be formed from the given letters.
 * Letters are NOT consumed — set membership check only.
 */
export function getWordsFromLetters(letters: string[]): string[] {
  const letterSet = new Set(letters)
  return WORDS.filter((word) => {
    for (const char of word) {
      if (!letterSet.has(char)) return false
    }
    return true
  })
}
`

  fs.writeFileSync(outPath, ts, 'utf-8')
  console.log(`\nWritten to ${outPath}`)
}

main().catch(console.error)
